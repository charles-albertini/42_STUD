#include "minishell.h"

int	ft_pwd()
{
	char *s;

	s = NULL;
	s = getcwd(s, 0);
	if(!s)
	{
		perror("pwd");
	}
	if(s)
	{
		printf("%s\n", s);
		free(s);
	}
	return(0);
}